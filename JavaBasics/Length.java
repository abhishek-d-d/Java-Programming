// This program demonstrate the length of the array in numbers
public class Length {
	public static void main(String[] args) {
		int array1[] = new int[10];
		int array2[] = {32,5,6,8,23,76,32};
		int array3[] = {4,3,2,1};
		
		//printing the length of each array using the length function
		
		System.out.println("The length of array1 is "+array1.length);
		System.out.println("The length of array2 is "+array2.length);
		System.out.println("The length of array3 is "+array3.length);
	}
	
}
